package com.codewithsohan;

public class ifelse {

    public static void main(String[] args){

        int numb1 = 500;
        int numb2 = 400;
        if (numb1 == numb2){
            System.out.println("Equal");
        }else {
            System.out.println("Not Equal");
        }

        //boolean
        System.out.println(100 == 300);
        System.out.println(2000 == 2000);
        System.out.println(800 < 1000);
        System.out.println(800 > 1000);
        System.out.println(500 == 500);
        System.out.println(500 < 500);
        System.out.println(500 <= 500);
        System.out.println(500 > 500);
        System.out.println(500 >= 500);

        System.out.println(300 !=100);
        System.out.println(400 != 400);
        System.out.println(400 != 300);


        int a = 50;
        int b = 60;
        if (a > b){
            System.out.println(a);
        } else {
            System.out.println(b);
        }

    }

}
